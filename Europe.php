       <p style="font-size: 1.2rem;">Il en va de soi que sortir de la qualification dans <a href="https://en.wikipedia.org/wiki/2022_FIFA_World_Cup_qualification_(UEFA)"> cette région </a> est un défi d'autant plus laborieux. Seulement 13 pays obtiendront leurs laisser-passer pour la phase finale au <a href="https://www.fifa.com/tournaments/mens/worldcup/qatar2022"> <strong>Qatar</strong> en décembre 2022 </a> et ce sur les 55 pays membre de <strong>l'UEFA</strong> (<a href ="https://www.uefa.com/"> Union of European Football Associations </a>). Les <a href="https://en.wikipedia.org/wiki/FIFA_World_Cup_qualification"> qualifications </a> comportent ainsi en première phase un tournoi à la ronde incluant 5 groupes de 5 équipes et 5 groupes de 6 équipes. Les gagnants des 10 groupes sont automatiquement qualifiés pour la <a href="https://en.wikipedia.org/wiki/FIFA_World_Cup"> <strong> Coupe du Monde </strong></a> et les 10 deuxièmes de chaque groupe ainsi que les 2 meilleures équipes de la <a href="https://en.wikipedia.org/wiki/UEFA_Nations_League"> Coupe des Nations </a>, rentrent dans un mini-tournoi à simple élimination pour qualifier les 3 dernières équipes. 

