  <h2 class="text-center"> Qualifications dans la zone Asie</h2>
  <br/>
  <br/>

  <p style="font-size: 1.2rem;">La <strong>fédération asiatique de football</strong> (<a href="https://en.wikipedia.org/wiki/Asian_Football_Confederation"> l'AFC </a>) desservant le plus gros continent du monde, autant en population qu'en superficie, bénéficie que d'une maigre 4.5(4 places en qualifications et une place inter-confédération) places de <strong>qualifications</strong> pour la <a href="https://en.wikipedia.org/wiki/FIFA_World_Cup"> <strong> Coupe du Monde </strong> </a> <a href="https://www.fifa.com/tournaments/mens/worldcup/qatar2022"> qatari de 2022 </a>. Le Qatar étant le pays hôte cependant, il bénéficie d'une qualification automatique pour la <a href="https://en.wikipedia.org/wiki/FIFA_World_Cup#Final_tournament"> ronde finale </a>. Le <strong>tournoi</strong> est structuré sous 4 rondes. Une <strong>ronde préliminaire</strong> incluant les équipes classées de <a href="https://www.fifa.com/fifa-world-ranking/men?dateId=id13471"> 35e à 46e pour </a> ressortir 6 pays en vue de la deuxième ronde. Celle-ci inclue les 40 meilleures <strong>équipes</strong> du continent, soit 8 groupes de 5 équipes qui s'affrontent en <a href="https://en.wikipedia.org/wiki/Round-robin_tournament"> tournoi à la ronde </a>. À l'issue de la deuxième ronde, les 8 <strong>gagnants</strong> des groupes et les 4 meilleurs deuxièmes se qualifieront pour le troisième tour. Ces douze <strong>équipes</strong> s'affronteront sur 2 groupes et les 2 meilleurs équipes de chaque groupe se qualifient. Les 2 troisièmes sont finalement envoyé dans un <a href="https://en.wikipedia.org/wiki/FIFA_World_Cup_qualification#Intercontinental_play-offs"> tournoi inter-confédération </a> pour la dernière place disponible.</P>
