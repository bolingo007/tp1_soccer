    <p style="font-size: 1.2rem;">L'institution internationale qu'est <a href="https://en.wikipedia.org/wiki/FIFA_World_Cup"> la <strong> Coupe du Monde de Soccer </strong></a> (ou <a href="https://en.wikipedia.org/wiki/Association_football">Football</a>) débutât son premier tournoi en <a href="https://en.wikipedia.org/wiki/1930_FIFA_World_Cup"> Uruguay en 1930 </a>. Sous l'égide de son premier président <a href="https://en.wikipedia.org/wiki/Jules_Rimet"> Jules Rimet </a>, la FIFA (<a href="https://www.fifa.com/">Fédération International de Football Association </a>) organisa la première <strong> Coupe du Monde </strong> qui fut contesté par un total de 13 équipes de trois continents. Remportée par le pays hôte, cette première édition démontra dès lors l'énorme popularité de cette compétition qui saura perdurée pour <a href="https://www.britannica.com/sports/World-Cup-football"> 90 ans et 21 éditions </a> du tournoi aux 4 ans. En effet, dès la deuxième édition de 1934, le tournoi fut grandement expandu, invitant 16 équipes en phase finale jusqu'à 32 à partir de 1998 et 46 pour l'édition à venir aux États-Unis en 2026. Attirant plus de <a href="https://en.wikipedia.org/wiki/List_of_most-watched_television_broadcasts"> 35 milliards de téléspectateurs </a> cumulés à travers les années, la <a href="https://en.wikipedia.org/wiki/FIFA_World_Cup"> <strong> Coupe du Monde </strong> de soccer </a> est l'un des événements sportifs les plus populaire de l'histoire de l'humanité. L'impact socio-économique d'une telle compétition pousse les États hôtes à payer des <a href="https://en.wikipedia.org/wiki/FIFA_World_Cup_hosts"> sommes faramineuses </a> en installations sportives et logistiques afin d'obtenir la présentation de la compétition dans leur pays. </p>

    <p style="font-size: 1.2rem;"><a href="https://digitalshowcase.lynchburg.edu/cgi/viewcontent.cgi?article=1159&context=utcp"> Les retombées touristiques et le rayonnement à l'international </a> poussent, en effet, plusieurs pays à payer des sommes énormes afin de porter leurs candidatures pour héberger le plus gros tournoi de <strong>soccer</strong> au même titre que les tout aussi prestigieux <a href="https://en.wikipedia.org/wiki/Olympic_Games"> Jeux Olympiques </a>. </p>
