    <p style="font-size: 1.2rem;">  D'un côté, le <strong>CONMEBOL</strong> (CONfederacion SudaMEricana de FutBOL) délimite ses représentants lors d'un simple tournoi à la ronde de 10 équipes ou les quatre meilleures équipes se qualifient automatiquement et la 
      cinquième avancera au tournoi <a href="https://en.wikipedia.org/wiki/FIFA_World_Cup_qualification#Intercontinental_play-offs"> inter-confédération </a> pour la dernière place disponible. </p>
