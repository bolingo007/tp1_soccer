    <p style="font-size: 1.2rem;">Du côté de l'hémisphère nord, la <strong> CONCACAF </strong> (Confédération de football d'Amérique du Nord,
     d'Amérique centrale et des Caraïbes) présente un tournoi en 3 étapes. Premièrement, les 6 meilleurs équipes s'affrontent dans <a href="https://en.wikipedia.org/wiki/Hexagonal_(CONCACAF)#:~:text=In%20association%20football%2C%20the%20term,six%20remaining%20teams%20in%20CONCACAF"> "l'Hexagone" </a> et les 3 meilleurs équipes se qualifient automatiquement, la quatrième affrontera le gagnant d'un tournoi
     impliquant les 29 autres équipes sous 8 groupes pour s'assurer une place dans le tournoi inter-confédération.  </p>

