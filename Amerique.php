    <h2>Les fédérations américaines</h2>
    <p style="font-size: 1.2rem;"> Malgré la domination <a href="./Europe.php"> Européenne </a> de l’ère moderne, <strong>les Amériques</strong> (plus particulièrement <a href="https://en.wikipedia.org/wiki/CONMEBOL"> l’Amérique du Sud </a>) sont reconnues comme des superpuissances de <a href="https://en.wikipedia.org/wiki/Association_football"> soccer </a>. En effet, le <a href="https://en.wikipedia.org/wiki/Brazilian_Football_Confederation"> Brésil </a> est le pays le plus titré de l’histoire de la <a href="https://en.wikipedia.org/wiki/FIFA_World_Cup"> <strong> Coupe du Monde </strong> </a> avec 5 championnats (1958, 1962, 1970, 1994 et 2002) et <a href="https://en.wikipedia.org/wiki/Argentine_Football_Association"> l’Argentine </a>, avec ces 2 victoires (1978 et 1986). 
      Les qualifications de la <a href="https://en.wikipedia.org/wiki/FIFA_World_Cup"> <strong> Coupe du Monde </strong> de la FIFA </a> au <a href="https://www.fifa.com/tournaments/mens/worldcup/qatar2022"> Qatar en 2022 </a> offre 4.5 places pour les équipes d'Amérique du Sud (<a href="https://en.wikipedia.org/wiki/CONMEBOL"><strong>CONMEBOL</strong></a>) et 3.5 pour la fédération Nord-Américaine de soccer (<a href="https://en.wikipedia.org/wiki/CONCACAF"><strong> CONCACAF </strong></a>). Deux tournois de qualifications
      séparés sont ainsi disputé en vue de la <a href="https://en.wikipedia.org/wiki/FIFA_World_Cup"> <strong> Coupe du Monde </strong> </a>. </p>
    <br/>
    <br/>
    <h2> CONMEBOL: CONfederacion SudaMEricana de FutBOL</h2>
    <div class="col-md-12">
